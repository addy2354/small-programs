class Rectangle:
    def     init     (s):
        print("Rectangle")
    def Area(s,l,w):
        s.l=l
        s.w=w
        print("Area of Rectangle is: ",s.l*s.w)
class Square:
    def     init     (s):
        print("Square")
    def Area(s,S):
        s.a=S
        print("Area of square is: ",s.a*s.a)
class Triangle:
    def     init     (s):
        print("Triangle")
    def Area(s,b,h):
        s.b=b
        s.h=h
        a=(1/2)*s.b*s.h
        print("Area of Triangle is: ",a)
